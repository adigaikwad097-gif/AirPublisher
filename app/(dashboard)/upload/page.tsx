import { Card, CardContent, CardHeader, CardTitle, CardDescription } from '@/components/ui/card'
import { Button } from '@/components/ui/button'
import { Badge } from '@/components/ui/badge'
import { Upload as UploadIcon, Video, FileVideo, Sparkles } from 'lucide-react'
import { UploadForm } from '@/components/upload/upload-form'
import { getCurrentCreator } from '@/lib/db/creator'
import Link from 'next/link'

// Force dynamic rendering - this page uses searchParams
export const dynamic = 'force-dynamic'

export default async function UploadPage({
  searchParams,
}: {
  searchParams?: Promise<{ profile?: string }> | { profile?: string }
}) {
  // Handle searchParams (might be Promise in newer Next.js)
  const params = searchParams instanceof Promise ? await searchParams : searchParams
  
  let creator = null
  try {
    creator = await getCurrentCreator(params?.profile)
  } catch (error: any) {
    console.error('[UploadPage] Error fetching creator:', error?.message || String(error))
  }

  if (!creator) {
    return (
      <div className="space-y-8">
        <div>
          <h1 className="text-4xl font-extrabold mb-2 text-white">Upload Content</h1>
          <p className="text-white/70 text-sm uppercase tracking-[0.4em]">
            Please complete your creator profile first.
          </p>
        </div>
        <Card className="bg-white/5 border-white/10">
          <CardContent className="pt-6">
            <div className="text-center py-8">
              <p className="text-lg font-semibold mb-4 text-white/90">
                Complete your creator profile to start uploading content.
              </p>
              <Link href="/setup">
                <Button size="lg" className="bg-[#89CFF0] text-black hover:bg-[#89CFF0]/90">
                  Set Up Profile
                </Button>
              </Link>
            </div>
          </CardContent>
        </Card>
      </div>
    )
  }

  return (
    <div className="space-y-8">
      <div className="mb-8">
        <h1 className="text-4xl font-extrabold mb-2 text-white">Upload Content</h1>
        <p className="text-white/70 text-sm uppercase tracking-[0.4em]">
          Upload UGC content or manage AI-generated content from AIR Ideas
        </p>
      </div>

      <div className="grid gap-6 md:grid-cols-2">
        {/* UGC Upload */}
        <Card className="bg-white/5 border-white/10">
          <CardHeader>
            <div className="flex items-center gap-3">
              <div className="p-2 bg-[#89CFF0]/20 rounded-lg">
                <FileVideo className="h-6 w-6 text-[#89CFF0]" />
              </div>
              <div>
                <CardTitle className="text-white">Upload UGC Content</CardTitle>
                <CardDescription className="text-white/70">
                  Upload your own video content
                </CardDescription>
              </div>
            </div>
          </CardHeader>
          <CardContent>
            <UploadForm creatorUniqueIdentifier={creator.unique_identifier} />
          </CardContent>
        </Card>

        {/* AI-Generated Content */}
        <Card className="bg-white/5 border-white/10">
          <CardHeader>
            <div className="flex items-center gap-3">
              <div className="p-2 bg-[#89CFF0]/20 rounded-lg">
                <Sparkles className="h-6 w-6 text-[#89CFF0]" />
              </div>
              <div>
                <CardTitle className="text-white">AI-Generated Content</CardTitle>
                <CardDescription className="text-white/70">
                  Manage content from AIR Ideas
                </CardDescription>
              </div>
            </div>
          </CardHeader>
          <CardContent>
            <div className="space-y-4">
              <p className="text-sm text-white/70">
                Content generated by AIR Ideas will automatically appear here as
                drafts. You can edit, schedule, or publish them directly.
              </p>
              <Button variant="outline" className="w-full bg-white/10 text-white hover:bg-white/20 border-white/10">
                View AI Drafts
              </Button>
            </div>
          </CardContent>
        </Card>
      </div>

      {/* Upload Guidelines */}
      <Card className="bg-white/5 border-white/10">
        <CardHeader>
          <CardTitle className="text-white">Upload Guidelines</CardTitle>
        </CardHeader>
        <CardContent>
          <ul className="space-y-2 text-sm text-white/70">
            <li>• Supported formats: MP4, MOV, AVI (max 500MB)</li>
            <li>• Recommended resolution: 1080p or higher</li>
            <li>• Add a compelling title and description for better performance</li>
            <li>• Select the target platform before scheduling</li>
            <li>• Thumbnails are automatically generated but can be customized</li>
          </ul>
        </CardContent>
      </Card>
    </div>
  )
}

